import React, {Component} from 'react';
import {connect} from 'react-redux';

import Typography from '@material-ui/core/Typography';
import Dialog from '@material-ui/core/Dialog';
import DialogTitle from '@material-ui/core/DialogTitle';
import DialogContent from '@material-ui/core/DialogContent';
import DialogContentText from '@material-ui/core/DialogContentText';
import DialogActions from '@material-ui/core/DialogActions';
import Button from '@material-ui/core/Button';

import {getEvents, importEventResults, transformEventDb} from '../actions/api';
import activeStorageUpload from '../actions/upload';
import Grid from '@material-ui/core/Grid/Grid';
import {invalidateRankings} from '../actions/util';
import CircularProgress from '@material-ui/core/es/CircularProgress/CircularProgress';
import Select from '@material-ui/core/Select';
import MenuItem from '@material-ui/core/MenuItem';

class EventTransformDialog extends Component {

  constructor(props) {
    super(props);
    this.fileInput = React.createRef();
    this.state = {error: null, inProgress: false};
  }

  import = async () => {
    if(this.fileInput.current.files.length === 0) return;
    this.setState({inProgress: true});
    const file = this.fileInput.current.files[0];
    try {
      const results = await this.props.transformEventDb(this.props.selectedSeason, this.props.event.id, file);
      if(results.error) {
        this.setState({error: results.payload.response.error});
      } else {
        const url = URL.createObjectURL(results.payload);
        const anchorElement = document.createElement('a');
        anchorElement.href = url;
        anchorElement.download = `${this.props.event.slug}.db`;
        anchorElement.click();
        anchorElement.remove();
        URL.revokeObjectURL(url);
      }
    } catch (ex) {
      console.log(ex);
    }
    this.setState({inProgress: false});
  };

  render () {
    if(this.props.event == null) return null;
    const {error} = this.state;
    const {event} = this.props;
    return <Dialog
        open={event !== null}
        onClose={this.props.onClose}
        aria-labelledby="form-dialog-title"
    >
      <DialogTitle id="form-dialog-title">Configure DB for {event.name}</DialogTitle>
      <DialogContent>
        <DialogContentText>
          Upload the DB generated by the scoring system here

          {error && <Grid item xs={12}>
            <Typography color="error">Error: {error}</Typography>
          </Grid> }
        </DialogContentText>
        <input
            ref={this.fileInput}
            type='file'
            hidden
            accept='.db'
            onChange={() => this.forceUpdate()}
        />
        <Button
            type='file'
            onClick={() => {
              this.fileInput.current.click();
            }}
        >
          Choose File <Typography variant="caption" style={{'margin-left': '0.5em', 'text-transform': 'none'}}>
          { this.fileInput.current && this.fileInput.current.files.length > 0 ? this.fileInput.current.files[0].name : 'No file chosen'}
          </Typography>
        </Button>

      </DialogContent>
      <DialogActions>
        <Button onClick={this.props.onClose} color="primary">
          Cancel
        </Button>
        { this.state.inProgress ?
           <CircularProgress size="1.5em"/> :
           <Button onClick={this.import} color="primary" disabled={this.fileInput.current && this.fileInput.current.files.length === 0}>
          Configure
        </Button> }
      </DialogActions>
    </Dialog>;
  }
}



const mapStateToProps = (state, ownProps) => {
  if (state.events && ownProps.id) {
    return {
      event: state.events[ownProps.id]
    };
  }
  return {
    event: null
  };
};

const mapDispatchToProps = {
  transformEventDb
};

export default connect(mapStateToProps, mapDispatchToProps)(EventTransformDialog);